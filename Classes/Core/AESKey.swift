import Foundation

@inline(__always) public func aesKey() -> [UInt8] {
    return [
        0x92-0x1C, 0xA2-0x33, 0x39+0x30, 0x42+0x2A, 0x6B-0x0A,
        0x40+0x21, 0x81-0x18, 0xA8-0x47, 0x69+0x09, 0xC4-0x50,
        0x7F-0x16, 0x88-0x15, 0x0F+0x65
    ]
}
